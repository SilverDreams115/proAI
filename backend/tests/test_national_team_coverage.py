class TestEntityResolutionAfterMigration:
    def test_find_team_prefers_canonical_via_moved_alias(self, tmp_path):
        """After v14 runs, find_team_by_alias('Croacia', 'croatia') returns
        Croatia (real, is_placeholder=False), not the Croacia placeholder."""

        from app.db.migrations import _migrate_to_v14
        from app.models.tables import TeamModel, TeamAliasModel
        from app.repositories.entity_repository import EntityRepository

        session = _make_session(tmp_path, "resolve.db")
        try:
            canonical = TeamModel(name="Croatia", country="HR", is_placeholder=False)
            placeholder = TeamModel(name="Croacia", country=None, is_placeholder=True)
            session.add_all([canonical, placeholder])
            session.flush()
            session.add(TeamAliasModel(team=placeholder, alias="Croacia", normalized_alias="croacia"))
            session.add(TeamAliasModel(team=canonical, alias="Croatia", normalized_alias="croatia"))
            session.commit()

            with session.get_bind().begin() as conn:
                _migrate_to_v14(conn)

            session.expire_all()
            repo = EntityRepository(session)
            result = repo.find_team_by_alias("Croacia", "croatia")
            assert result is not None
            assert result.id == canonical.id
            assert result.is_placeholder is False
        finally:
            session.close()

